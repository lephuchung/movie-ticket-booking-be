CREATE DATABASE IF NOT EXISTS MovieTheater;

USE MovieTheater;

CREATE TABLE Theaters (
    TheaterId VARCHAR(7) PRIMARY KEY,
    Name VARCHAR(255),
    TotalRoom INT,
    Location VARCHAR(255)
);

CREATE TABLE Room (
    RoomId VARCHAR (7) PRIMARY KEY,
    TotalSeat INT,
    Name VARCHAR(255),
    TheaterId VARCHAR(7),
    FOREIGN KEY (theaterId) REFERENCES Theaters(theaterId)
);

CREATE TABLE Seats (
    SeatId VARCHAR(7) PRIMARY KEY,
    SeatNumber VARCHAR(10),
    Line VARCHAR(10),
    RoomId VARCHAR (7),
    FOREIGN KEY (RoomId) REFERENCES Room(RoomId)
);

CREATE TABLE Movies (
    MovieId VARCHAR(7) PRIMARY KEY,
    Title VARCHAR(255),
    Description TEXT,
    Genre VARCHAR(100),
    ReleaseDate DATETIME,
    Rating FLOAT,
    Duration INT,
    Director VARCHAR(255),
    PosterUrl VARCHAR(255)
);

CREATE TABLE Showtimes (
    ShowtimeId VARCHAR(10) PRIMARY KEY,
    StartTime DATETIME,
    EndTime DATETIME,
    SeatStatus VARCHAR(20),
    Price FLOAT,
    Name VARCHAR(255),
    TheaterId VARCHAR(7),
    RoomId VARCHAR(7),
    MovieId VARCHAR(7),
    FOREIGN KEY (theaterId) REFERENCES Theaters(theaterId),
    FOREIGN KEY (roomId) REFERENCES Room(roomId),
    FOREIGN KEY (movieId) REFERENCES Movies(movieId)
);

CREATE TABLE Users (
    UserId VARCHAR(9) PRIMARY KEY,
    Name VARCHAR(255),
    Password VARCHAR(255),
    Email VARCHAR(255),
    Phone VARCHAR(15),
    Role VARCHAR(50),
    CreateAt DATETIME,
    Status VARCHAR(20)
);

CREATE TABLE Payments (
    PaymentId VARCHAR(10) PRIMARY KEY,
    PaymentStatus VARCHAR(20),
    Amount FLOAT,
    PaymentTime DATETIME,
    PaymentMethod VARCHAR(50),
    UserId VARCHAR(9),
    FOREIGN KEY (userId) REFERENCES Users(userId)
);

CREATE TABLE Tickets (
    TicketId VARCHAR(10) PRIMARY KEY,
    SeatNumber VARCHAR(10),
    BookingTime DATETIME,
    TotalPrice FLOAT,
    PaymentStatus VARCHAR(20),
    UserId VARCHAR(9),
    ShowtimeId VARCHAR(10),
    FOREIGN KEY (userId) REFERENCES Users(userId),
    FOREIGN KEY (showtimeId) REFERENCES Showtimes(showtimeId)
);

